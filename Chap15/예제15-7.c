#include <stdio.h>

int sum(int, int);						// 함수선언 입력과 출력(포인터와 함수)가 같아야 함(line 7과 데이터 타입이 같아야 함)
										
int main(void)
{
	int (*fp)(int, int);				// 입력,출력있고 함수이름 대신 포인터로!
	int res;

	fp = sum;							// 함수명을 함수 포인터에 저장
	res = fp(10, 20);					// 함수 포인터로 함수를 호출
	printf("result : %d\n", res);

	return 0;
}

int sum(int a, int b)
{
	return (a + b);
}